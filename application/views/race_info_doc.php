		<span style="font-size:14px;"> <strong>（五）	其他</strong><br>
		1、	参赛选手的参赛用车、装备与护具自备，参赛者在比赛期间必须佩戴头盔、手套，安装比赛号码，否则裁判有权随时取消参赛资格并令其提前终止比赛。<br>
		2、	比赛中不得有抄近道、不得漏跑圈数，不得故意冲撞、排挤、刮蹭他人，比赛冲刺前100米不得走曲线、斜线冲刺或有其他阻挡行为，否则取消比赛成绩。<br>
		3、	个人、团体赛均采取电子计时为主、人工计时为辅的计时方式。<br>
		4、	比赛中容许携带轮胎、维修工具进行自行维修，不得借助外部力量维修。<br>
		5、	赛事主裁由中国自协指派，比赛规则如有未尽规定，参照中国自行车运动协会最新规则执行。<br>
		6、	赛事当日如遇一般性的刮风、下雨天气，比赛将照常，不延期、更改日期。<br>
		7、	赛事当日如遇雷雨、狂风、暴雨等特别恶劣天气或其他意外事件，比赛可能将被延迟、延期或择日举行或终止或被取消，请大家理解支持。<br>
		8、	如遇下雨等天气，裁判组有权根据赛事进展情况、天气状况等采取合并或分组、增减圈数、提前或推后发车等措施以保证顺利完成比赛，敬请车手注意现场公告、通知。因此导致的比赛异议，赛事组委会恕不受理投诉。也请车友提前到场随时关注现场通知及公告。<br>
		9、如因雷雨天气等因素导致比赛计时器无法工作，比赛成绩将以人工记录、摄像记录等为主，裁判组将只能提供奖励名次，无比赛用时及其他名次，敬请谅解。<br>
