		<h4 class="text-center"><strong>第二部分 友情提示</strong></h4>
		<strong>一、关于住宿、交通和餐饮</strong><br>
		●  <span class="red">北大车协只提供5月14日晚的固安县城住宿餐饮，不提供5月13日和5月15日晚的固安县城住宿餐饮。</span> <br>
		●  建议各高校在便利的情况下，集体包车前往。固安县位于北京南郊，交通非常便利，多条高速公路和国道联通，赛场和所有住宿点均位于县城中心及附近地区。<br>
		●  北大车协不负责5月14日晚需要在北京大学附近的食宿。如高校车协确有需要，请提前通过网络联系住宿点。<br>
		<strong>二、关于抵达固安、入住、就餐</strong><br>
		●  由于报名人数预计较多，单个宾馆或旅店肯定无法容纳，北大车协将根据报名情况，将所有报名人员分为数个住宿点，在每个住宿点均有北大车协会员值守。在报名结束以后，我们将及时提供详细住宿点名称、地址和方位。各校只需直接到达入住宾馆即可。<br>
		●  由于固安县城接待能力有限，5 月 14 日晚餐将采用盒饭形式，直接送达各个住宿点。<br>
		●  因固安食宿接待能力有限，北大车协将不提供15日早晨的早饭服务。请各高校车协选手自行准备。比赛场地和各住宿地点均处于城区，购物方便。<br>
		●  为了向参加联谊活动和比赛的高校车协成员提供良好的沟通机会，北大车协同永定河自行车运动公园协商，14 日晚高校车协选手可宿营场地。各高校车协需自带帐篷前往，每人需缴纳10元管理费用，用于场地卫生维护。<br>
		<strong>三、关于比赛</strong><br>
		●  所有组别（大学男子精英组、大学男子大众组、大学女子组、大学生接力组）需在<span class="red">5月15日由领队携带所有队员身份证、学生证原件以及200元/人次的电子计时器押金，到北大赛现场领取赛事套包</span> ，并及时下发。<br>
		（注：每个组别有一个号码和电子计时器，团体接力组共用一个号码和电子计时器，如果某队员参与大学男子精英组和大学生接力组，则其本人应缴纳 200 元的电子计时器押金，其所在接力组集体缴纳200元/组的电子计时器押金）<br>
		<br>
		例子：某队有4名队员，三男一女，每个男生均参加了北大赛的大学生男子精英组和大学生接力组，女生未参加大学生女子组，但参加大学生团体接力组。该四名队员组成一个接力组。<br><br>
		则领队应集中收取的计时器押金为：200 元/人（个人赛）*3+200 元/组（团体赛）=800 元<br><br>
		●  比赛期间，严禁随意横穿赛道，干扰比赛进行。一经发现，处罚将由现场专业裁判	做出，北大车协无权干涉或协调。<br>
