						<h2 align=center>北大车协交流赛历史荟萃</h2>
			
						<div class="div1">
						<span class="pstatus"></span><br />北大车协全国高校山地车交流赛，是北大车协十余年的精品活动，也是全国大学生自行车业余比赛中中最有号召力和影响力的比赛。经过多年努力，近年来，参赛范围不断扩大，参赛人数不断增多，“全国”一词也称得上是名副其实。因为今年北大赛加入了公路组，特此更名为北大车协全国高校自行车交流赛。这是北大赛历史上首次有公路车参赛，标志着北大赛的形式与内容的更进一步.<br /><br /><br />
						<h3>一、『八达岭』山地车拉力赛</h3>
						<span class="pstatus"></span><br /><span>北大车协举办大学生自行车比赛的历史，可以追溯到1998年。</span>在那个年代，自行车在国人的定义里，依然只有"凤凰"、"永久"和28大杠的样式。除专业运动员以外，国人对于自行车运动竞技知之甚少。在那样一个自行车行业方兴未艾的时期，北大车协在刚刚成立3年之后，即举办了”北大——八达岭“首都大学生自行车拉力赛。本项比赛不仅引发了当时国内自行车业界的积极反响，而且也极大地推动的高校自行车运动的发展。
						<br /><br /><br />
						<div align="center"><img src="<?=base_url()?>/assets/images/publicize/history_1.jpg" alt="八达岭海报" ></div><br />
						<span class="pstatus"></span><br />此后，北大车协多年来一直坚持举办大学生自行车比赛，很多初步接触到自行车竞技的大学生们，通过北大车协的大学生比赛找到了志同道合的朋友伙伴。同时，也促成了很多高校自行车社团的兴起。甚至很多高校自行车社团的口号和宗旨也仿照北大车协。一时间，高校车协在新世纪初期如雨后春笋般建立，高校自行车社团力量逐渐壮大，成为中国业余自行车当中一支充满潜力的群体。<br /><br /><br /><span>
						<h3>二、交流赛兴起</h3>
						<span class="pstatus"></span><br />北大车协举办大学生自行车比赛的历史，可以追溯到1998年。</span>但是，高校社团的一大问题——传承和发展，成为很多协会的绊脚石。不少建立之初风生水起的高校自行车社团，在成立三四年以后，迈不过老生毕业，无人接班的坎。然而，北大车协一直坚持发展，积极进步，在不断扩大协会影响力和规模的同时，一直坚持举办高校自行车比赛。2002年，北大车协重新举办高校山地车比赛，比赛地点选在了京北的铁壁银山（现银山塔林景区）。
						<br /><br /><br />
						<img src="<?=base_url()?>/assets/images/publicize/history_2.jpg" alt="2002年 铁壁银山首都大学生山地车交流赛" width="970"><br />
						<span class="pstatus"></span><br />本次比赛上，“交流赛”一词应运而生。交流，体现出本场比赛的性质是一次“以车会友”的高校自行车爱好者聚会。比赛，又凸显出更高、更快、更强的体育竞技精神。虽然首届比赛参赛队员人数较少，也仅限于北京高校，但是无疑这为后来的比赛奠定了坚实的基础。
						<br /><br /><br />
						<img src="<?=base_url()?>/assets/images/publicize/history_3.jpg" alt="2002年参赛各队合影" width="970"><br />
						<h6 align=center>2002年参赛各队合影</h6>
						<h3>三、未名湖、青龙湖山地车交流赛</h3>
						<span class="pstatus"></span><br />2002年比赛成功举办之后，北大车协看到了办好高校业余自行车竞赛的重大意义。因此，“办赛”成为历年北大车协绝对不可撼动的“保留节目”。从2002年到2006年，北大车协还把高校自行车比赛搬到了燕园之中，未名湖畔。“北大车协未名湖越野夺标赛”应运而生。此后，比赛规模不断扩大，未名湖畔已经无法满足比赛需要。因此，京南青龙湖成为了北大车协新的办赛地点。2007年，“北京高校青龙湖山地车交流赛”举办。此后3年，青龙湖连续举办了3届交流赛，比赛的规模和名气也越来越响。
						<br /><br /><br />
						<img src="<?=base_url()?>/assets/images/publicize/history_4.jpg" alt="2007年 北京高校青龙湖山地车交流赛" width="970"><br />
						<h6 align=center>2007年 北京高校青龙湖山地车交流赛</h6>
						<h3>四、『北大车协全国高校山地车交流赛』名称的确立</h3>
						<span class="pstatus"></span><br />2010-2012年，北大车协在十三陵镇康陵村连续举办三届“北大车协全国高校山地车交流赛”。其中，2010年的比赛已经是第十届比赛。自此，比赛的名称确定，赞助商与我们建立了稳定的合作关系。近年来，UCC·环球自行车成为北大车协全国高校山地车交流赛的老朋友，为本场比赛提供多年的赞助和支持。
						<br /><br /><br />
						<h3>五、近期日趋成熟的交流赛</h3>
						<span class="pstatus"></span><br />2013年，北大车协又与探路者旗下时尚骑行品牌“阿肯诺ACANU”建立合作伙伴关系。阿肯诺为2012年北大车协第十一届全国高校山地车交流赛提供了物资、奖品、资金和工作人员的支持。本次比赛，参赛高校范围扩展至广大的北方地区。河北、天津、山东、山西、河南、陕西、辽宁、吉林、黑龙江各省高校会员齐聚北京。参赛人数规模超过400人。这样的比赛规模，放至全国业余自行车比赛来看，也是少有的。更加不易的是，十一年来，北大车协作为一个学生社团，依靠自己的力量，联合厂商、政府的力量，坚持了十一年时间。在风雨当中不断积累经验，坚持办赛，为全国高校自行车社团创造了这样一场来之不易的交流盛会。
						<br /><br /><br />
						<img src="<?=base_url()?>/assets/images/publicize/history_6.jpg" alt="2013年北大车协 第十一届全国高校山地车交流赛" width="970"><br />
						<h6 align=center>2013年北大车协 第十一届全国高校山地车交流</h6>
						<span class="pstatus"></span><br />2014年北大车协吸取前几届比赛中的经验教训，锐意进取，与UCC、阿肯诺深度合作，加强办赛能力，提高工作人员素质，并与延庆县政府合作，在延庆县妫河森林公园山地骑行赛道举办“UCC·阿肯诺杯”第十二届全国高校山地车交流赛。由于得到了厂商、政府的大力支持，本届比赛的水平和规模再上新台阶。参赛人数达到600余人，甚至吸引了澳门大学单车社的同学飞赴北京参赛。本场比赛的吸引力和号召力可见一斑。此外，参加比赛的高校车协除了参与交流晚会以外，还自发的组织了别开生面的“土特产品”小聚会。平日里身处祖国各地的高校骑行同仁们，此时齐聚北京。而创造这些条件的，是北大车协。
						<br /><br /><br />
						<img src="<?=base_url()?>/assets/images/publicize/history_5.jpg" alt="2014年北大车协全体比赛工作人员合影" width="970"><br />
						<h6 align=center>2014年北大车协全体比赛工作人员合影</h6>
						<span class="pstatus"></span><br />2015-2017年北大车协在河北省廊坊市固安县永定河自行车运动公园连续举办了三届规模浩大的交流赛，交流赛吸引了来自全国各地的大学生车友前来竞技、切磋交流，可谓是中国大学生自行车届的一大盛事。

						
						
