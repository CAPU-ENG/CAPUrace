	<h3 class="text-center">比赛流程</h3>
	<hr>
    <div class="artcontent">
    <span style="font-size:14px;">
    	<strong><strong style="font-size:14px;line-height:21px;white-space:normal;">（一）&nbsp;</strong>签到并领取<span class="red">北大赛四个组别</span>的赛事套包</strong>
		<br>
		<br>
		
		1. 选手需提前抵达赛场，由各高校代表队领队收齐参赛人员学生证和电子计时器押金（200元/人），并将电子计时器押金装入北大车协提供的指定档案袋中，到签到处领取赛事套包。<br>
		2. <span class="red">每个选手参与的每个组别均有对应号码和赛事套包。一个号码对应一个电子计时器。</span><strong>团体赛为集体号码，每队共用一个电子计时器。</strong>（例：男选手A参与大学男子组和大学接力组比赛，将领取到大学男子组对应的号码和计时器，另外，同他所在的接力队伍一起领取对应的集体号码和计时器）<br>
		3. 赛事套包内含：<br>
		a)号码牌与号码布<br>
		b)电子计时器1个<br>
		c)曲别针若干<br>
		d)扎带若干<br>
		e)赛事纪念品一份（赠送）<br>

	</span> 
		<p>
			<span style="font-size:14px;"><br>
		</span> 
		</p>


		<span style="font-size:14px;"> <strong>（二）	根据现场通知和比赛时间表，选手按时检录、参赛</strong><br>
		1.   检录时请安装、佩戴好号码牌与号码布，并安装好电子计时器。<br>
		2.   检录时，将检查选手：<br>
		a)   是否为本组别参赛队员；<br>
		b)   是否安全佩戴头盔或其他护具；<br>
		c)   是否使用符合<strong>比赛规定</strong>和<strong>安全</strong>的车辆（规定见后）<br>
		d)   是否和所持证件（身份证、学生证）一致<br>
		3.  <strong>比赛裁判组将根据报名情况和现场情况（时间、天气、实际参赛人数、赛道容量）</strong>，<span class="red">进行分组或合组发车。</span><br>

		</span> 
		<p>
			<span style="font-size:14px;"><br>
		</span> 
		</p>


		<span style="font-size:14px;"> <strong>（三）比赛结束后，请到指定地点退还电子计时器，并领回对应数额的押金。</strong><br>
		<br>
		<span style="font-size:14px;"> <strong>（四）	获奖选手，需凭本人身份证和学生证原件，在规定颁奖时间到指定地点领奖。</strong><br>
		</span>
